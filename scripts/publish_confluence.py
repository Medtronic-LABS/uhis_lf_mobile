def parse_sections():
    """
    Parse section_registry.dart → list of (sectionId, programmes, priority, fieldCount).
    Walks through FormSection( blocks and extracts the three header fields.
    """
    path = REPO_ROOT / "lib/features/visit/composer/section_registry.dart"
    src = path.read_text()

    # Collect (sectionId, programmes, priority) triples
    # Pattern: after FormSection( we expect sectionId, programmes, priority in that order
    blocks = re.findall(
        r"FormSection\(\s*sectionId:\s*'([^']+)',\s*programmes:\s*\{([^}]+)\},\s*priority:\s*(\d+),",
        src,
    )

    # Count FieldDef occurrences per section block
    section_src_blocks = re.split(r"FormSection\(", src)[1:]

    results = []
    for i, (sid, progs_raw, prio) in enumerate(blocks):
        progs = re.sub(r"Programme\.", "", progs_raw).replace(" ", "")
        # count FieldDef entries in this block (up to next FormSection)
        block_text = section_src_blocks[i] if i < len(section_src_blocks) else ""
        field_count = block_text.count("FieldDef(")
        results.append((sid, progs, int(prio), field_count))

    results.sort(key=lambda x: x[2])
    return results


def parse_db_tables():
    """
    Parse app_database.dart → (schema_version, table_names, migration_notes).
    """
    path = REPO_ROOT / "lib/core/db/app_database.dart"
    src = path.read_text()

    version = re.search(r"static const int schemaVersion = (\d+)", src)
    schema_version = int(version.group(1)) if version else "?"

    tables = re.findall(r"static const String table\w+ = '([^']+)'", src)

    # Migration notes: parse comment lines after each `if (from < N)` guard
    migration_rows = []
    for m in re.finditer(r"if \(from < (\d+)\) \{[^\n]*\n\s*// (.+)", src):
        ver = m.group(1)
        note_text = m.group(2).strip()
        migration_rows.append((f"v{ver}", note_text))

    return schema_version, tables, migration_rows


def parse_cds_alerts():
    """
    Parse cds_rules.dart → list of (alertId, severity, action).
    """
    path = REPO_ROOT / "lib/features/visit/composer/cds_rules.dart"
    src = path.read_text()

    # Find CdsAlert(...) blocks and extract alertId, severity, action
    blocks = re.findall(
        r"CdsAlert\(\s*alertId:\s*'([^']+)',\s*severity:\s*CdsSeverity\.(\w+),.*?action:\s*CdsAction\.(\w+)",
        src,
        re.DOTALL,
    )
    # Deduplicate (some alerts appear in both const and dynamic form)
    seen = set()
    results = []
    for alert_id, severity, action in blocks:
        if alert_id not in seen and alert_id != "alertId":
            seen.add(alert_id)
            results.append((alert_id, severity, action))
    return results


def parse_programmes():
    """
    Parse programme.dart → list of programme enum values (excluding 'unknown').
    """
    path = REPO_ROOT / "lib/core/models/programme.dart"
    src = path.read_text()
    # Enum body is between the first { and the first ;
    enum_body = re.search(r"enum Programme \{([^}]+)\}", src)
    if not enum_body:
        return []
    values = re.findall(r"^\s+(\w+),?$", enum_body.group(1), re.MULTILINE)
    return [v for v in values if v not in ("unknown",)]


def parse_pubspec():
    """
    Parse pubspec.yaml → (app_version, key_deps dict).
    """
    path = REPO_ROOT / "pubspec.yaml"
    src = path.read_text()

    version = re.search(r"^version:\s*(.+)", src, re.MULTILINE)
    app_version = version.group(1).strip() if version else "?"

    key_deps = {}
    for pkg in ["flutter_secure_storage", "go_router", "provider",
                "sqflite_sqlcipher", "atlassian_python_api"]:
        m = re.search(rf"^\s+{re.escape(pkg)}:\s*(.+)", src, re.MULTILINE)
        if m:
            key_deps[pkg] = m.group(1).strip()

    return app_version, key_deps


def parse_cdss_algorithms():
    """
    Parse lib/core/cdss/ → list of (class_name, trigger_condition) per calculator.
    """
    cdss_dir = REPO_ROOT / "lib/core/cdss"
    results = []
    for dart_file in sorted(cdss_dir.glob("*_calculator.dart")):
        src = dart_file.read_text()
        cls = re.search(r"class (\w+Calculator)", src)
        # Look for the trigger condition in comments or logic
        trigger = re.search(r"trigger\s*=\s*([^;]+);", src)
        if cls:
            trigger_val = trigger.group(1).strip() if trigger else "—"
            results.append((cls.group(1), trigger_val))
    return results


def parse_gorouter_routes():
    """
    Parse app_router.dart or main.dart → routes list.
    Looks for GoRoute(path: '...') entries.
    """
    candidates = [
        REPO_ROOT / "lib/core/app_router.dart",
        REPO_ROOT / "lib/app_router.dart",
        REPO_ROOT / "lib/main.dart",
    ]
    for path in candidates:
        if path.exists():
            src = path.read_text()
            routes = re.findall(r"path:\s*'([^']+)'", src)
            if routes:
                return routes
    return []


