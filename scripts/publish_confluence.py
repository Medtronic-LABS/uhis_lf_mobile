# ---------------------------------------------------------------------------
# Confluence storage-format helpers
# ---------------------------------------------------------------------------
def h(level, text):
    return f"<h{level}>{text}</h{level}>"

def p(text):
    return f"<p>{text}</p>"

def ul(items):
    return "<ul>" + "".join(f"<li>{i}</li>" for i in items) + "</ul>"

def code(content, language="bash"):
    return (
        f'<ac:structured-macro ac:name="code" ac:schema-version="1">'
        f'<ac:parameter ac:name="language">{language}</ac:parameter>'
        f"<ac:plain-text-body><![CDATA[{content}]]></ac:plain-text-body>"
        f"</ac:structured-macro>"
    )

def table(headers, rows):
    head = "<tr>" + "".join(f"<th><strong>{c}</strong></th>" for c in headers) + "</tr>"
    body = "".join(
        "<tr>" + "".join(f"<td>{c}</td>" for c in row) + "</tr>"
        for row in rows
    )
    return f"<table>{head}{body}</table>"

def info(text):
    return (
        f'<ac:structured-macro ac:name="info" ac:schema-version="1">'
        f"<ac:rich-text-body><p>{text}</p></ac:rich-text-body>"
        f"</ac:structured-macro>"
    )

def warning(text):
    return (
        f'<ac:structured-macro ac:name="warning" ac:schema-version="1">'
        f"<ac:rich-text-body><p>{text}</p></ac:rich-text-body>"
        f"</ac:structured-macro>"
    )

def note(text):
    return (
        f'<ac:structured-macro ac:name="note" ac:schema-version="1">'
        f"<ac:rich-text-body><p>{text}</p></ac:rich-text-body>"
        f"</ac:structured-macro>"
    )


